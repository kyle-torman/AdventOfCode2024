        public override Task<Solution> SolvePart1Async(InputType inputType)
        {
            var input = Input.GetInput(inputType);

            var map = MultidimensionalArrayParser.Parse2DArray(input, Tile.Create);
            var visitedPositions = map.GetVisitedTilesFromStartingPosition();
            return Task.FromResult(new Solution($"Number of Visited Tiles: {visitedPositions}"));
        }

        public override Task<Solution> SolvePart2Async(InputType inputType)
        {
            var input = Input.GetInput(inputType);

            var map = MultidimensionalArrayParser.Parse2DArray(input, Tile.Create);
            var possiblePlacements = 0;
            for (var row = 0; row < map.NumberOfRows(); row++)
            {
                for (var column = 0; column < map.NumberOfColumns(); column++)
                {
                    var tile = map[row, column];
                    if (tile is not EmptyTile)
                    {
                        continue;
                    }

                    var tempMap = MultidimensionalArrayParser.Parse2DArray(input, Tile.Create);
                    ((EmptyTile)tempMap[row, column]).TreatAsObstacle = true;
                    if (tempMap.IsLoopDetected())
                    {
                        possiblePlacements++;
                    }
                }

                Console.WriteLine($"Finished row {row}");
            }

            return Task.FromResult(new Solution($"Maximum Obstruction Placements: {possiblePlacements}"));
        }

